package me.ixfan.wechatkit.common;

import com.google.gson.FieldNamingPolicy;
import com.google.gson.Gson;
import com.google.gson.GsonBuilder;
import com.google.gson.JsonObject;

/**
 * 调用微信接口的返回结果封装对象。
 *
 * @author Warren Fan
 */
public class WechatApiResult {

    /**
     * 微信接口返回码
     */
    private long errcode;
    /**
     * 接口返回结果说明
     */
    private String errmsg;

    /**
     * 上传图文消息内的图片后的图片URL
     */
    private String url;
    /**
     * 上传的媒体文件类型,分别有图片（image）、语音（voice）、视频（video）和缩略图（thumb），图文消息（news）
     */
    private String type;
    /**
     * 媒体文件/图文消息上传后获取的唯一标识
     */
    private String mediaId;
    /**
     * 媒体文件上传时间
     */
    private long createdAt;

    public static WechatApiResult instanceOf(JsonObject jsonResponse) {
        Gson gson = new GsonBuilder().setFieldNamingPolicy(FieldNamingPolicy.LOWER_CASE_WITH_UNDERSCORES).create();
        return gson.fromJson(jsonResponse, WechatApiResult.class);
    }

    public long getErrcode() {
        return errcode;
    }

    public void setErrcode(long errcode) {
        this.errcode = errcode;
    }

    public String getErrmsg() {
        return errmsg;
    }

    public void setErrmsg(String errmsg) {
        this.errmsg = errmsg;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getMediaId() {
        return mediaId;
    }

    public void setMediaId(String mediaId) {
        this.mediaId = mediaId;
    }

    public long getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(long createdAt) {
        this.createdAt = createdAt;
    }
}
